//Basic Pattern 2
public class decreasingPattern
{
    public static void main(String[] args)
    {
        int n=5;
        for(int i=1; i<=n;i++)
        {
            for(int j=i;j<=n;j++)
            {
                System.err.print("* ");
            }
            System.err.println();

        }
    }    
}

    